function RemoveExistingDummyRootFolder()
{
    Remove-Item -Recurse -Force $dummyRootFolder
}

function CreateFolder($folder)
{
    New-Item -ItemType Directory $folder
}

function GenerateFile ($versionName, $subPath, $chapter)
{
    $destFile = Join-Path $dummyRootFolder $versionName 
    
    $destFile = Join-Path $destFile $subPath
    
    $synth = New-Object System.Speech.Synthesis.SpeechSynthesizer
    $synth.SetOutputToWaveFile($syntTempFile)
    $synth.Speak($chapter+","+$versionName)
    $synth.Dispose();

    lame $syntTempFile $destFile
}


